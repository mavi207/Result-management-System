            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Mavia@7807");
            
            Statement st=con.createStatement();
            
            st.executeUpdate("insert into student(rollno,course,branchname,name,fathername,gender,dateofbirth) values('"+rollno+"','"+course+"','"+branchname+"','"+name+"','"+fathername+"','"+gender+"','"+Dateofbirth+"')");
