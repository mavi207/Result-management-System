    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rollno=(String)jTextField1.getText();
        String physics=(String)jTextField2.getText();
        String math=(String)jTextField3.getText();
        String em=(String)jTextField4.getText();
        String dbms=(String)jTextField5.getText();
        String os=(String)jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Mavia@7807");
            
            Statement st=con.createStatement();
            
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            
            if(rs.next()){
            st.executeUpdate("insert into result(rollno,physics,maths,em,dbms,os) values('"+rollno+"','"+physics+"','"+math+"','"+em+"','"+dbms+"','"+os+"')");
            JOptionPane.showMessageDialog(null,"Data Save Sucessfully");
            setVisible(false);
            new AddResult().setVisible(true); 
            }
            else{
            JOptionPane.showMessageDialog(null,"Roll Number is Not Registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

